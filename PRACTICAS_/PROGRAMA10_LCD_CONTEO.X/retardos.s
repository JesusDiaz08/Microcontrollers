	.include "p30F4013.inc"

    .GLOBAL _RETARDO_1S
    .GLOBAL _RETARDO_5ms
	.GLOBAL _RETARDO_15ms
	.GLOBAL _RETARDO_30ms

;|---------- FUNCION DE RETARDO DE 1 SEG ----------|
_RETARDO_1S:
	PUSH	W0  ; PUSH.D W0
	PUSH	W1
	
	MOV	#10,	    W1
CICLO2_1S:
    
	CLR	W0	
CICLO1_1S:	
	DEC	W0,	    W0
	BRA	NZ,	    CICLO1_1S	
    
	DEC	W1,	    W1
	BRA	NZ,	    CICLO2_1S
	
	POP	W1	; POP.D W0
	POP	W0
	RETURN

;|---------- FUNCION DE RETARDO DE 5 ms ----------|
_RETARDO_5ms:
	PUSH W0
	;MOV #10,W0
	MOV #0xB64, W0	    ; 0xB64 = 2916
CICLO_5ms:
	DEC W0, W0
	BRA NZ, CICLO_5ms
	
	POP W0
	RETURN

;|---------- FUNCION DE RETARDO DE 15 ms ----------|
_RETARDO_15ms:
	CALL _RETARDO_5ms
	CALL _RETARDO_5ms
	CALL _RETARDO_5ms
	RETURN	

;|---------- FUNCION DE RETARDO DE 30 ms ----------|
_RETARDO_30ms:
	CALL _RETARDO_15ms
	CALL _RETARDO_15ms
	RETURN